<h4>Профилированный и клееный брус</h4>
 
<p>Ровная поверхность профилированного цельного и клееного бруса дает возможность для реализации любых дизайнерских находок. <a href="http://www.makihouse.ru/countryhouse/greenside/houses/" title="деревянный дом клееный брус">Клееный брус</a> стал современной альтернативой профилированному брусу, ведь он наилучшим образом подчеркивает особенности дерева. </p>
 
<h4>Оцилиндрованное бревно</h4>
 
<p>Оцилиндрованное бревно из массива сосны – строительный материал, известный во всем мире. Дом, построенный из массивного бревна, несет на себе отпечаток истинно финского духа. Настоящие оцилиндрованные бревна дышат теплом и чистотой – ведь они делаются из лучшей лапландской древесины. </p>
 
<h4>Лапландский сухостой и золотая сосна</h4>
 
<p>Когда сосна достигает определенного возраста, ее ветви отпадают, и остается лишь небольшая крона у верхушки дерева. Ядровая древесина такой сосны – это чрезвычайно прочный и долговечный материал. Со временем неповаленное дерево превращается в сухостой.</p>
 
<p>При обработке древесины водой текстура дерева не меняется, и естественный рисунок остается подобно украшению. Каждый дом, построенный из золотой сосны или сухостоя, уникален, что еще раз подчеркивает ценность лапландской древесины. </p>
 
<p>Дома из золотой сосны и сухостоя несут на себе отпечаток давних традиций и мастерства плотников. </p>
 
